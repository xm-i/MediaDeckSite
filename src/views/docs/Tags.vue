      <li><strong>タグ属性</strong>: タグには名前だけでなく、読み仮名(Ruby)、ローマ字(自動変換)、説明、使用回数などのメタデータを保持できます。</li>
      <li><strong>タグエイリアス</strong>: タグの別名（エイリアス）を追加でき、表記ゆれを吸収して検索性を向上させます。</li>
